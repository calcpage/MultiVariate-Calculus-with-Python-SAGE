#1) Integration By Partial Fractions is really algebra!
show("#1) Integration By Partial Fractions is really algebra!")
show("integral(1/(x+1),x)=",integral(1/(x+1),x))
show("integral(1/(x^2+1),x)=",integral(1/(x^2+1),x))
show("integral(1/sqrt(1-x^2),x)=",integral(1/sqrt(1-x^2),x))
show("integral(1/((x+1)*(x-2)),x)=",integral(1/((x+1)*(x-2)),x))
show("integral(x/((x+1)*(x-2)),x)=",integral(x/((x+1)*(x-2)),x))
show("integral((x-1)/((x+1)*(x-2)*(x+3)),x)=",integral((x-1)/((x+1)*(x-2)*(x+3)),x))

